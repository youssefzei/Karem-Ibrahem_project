document.getElementById("lang-switcher").addEventListener("click", function () {
    const body = document.body;
    if (body.getAttribute("dir") === "rtl") {
        body.setAttribute("dir", "ltr");
    } else {
        body.setAttribute("dir", "rtl");
    }
});